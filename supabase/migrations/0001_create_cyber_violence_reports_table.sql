CREATE TABLE public.cyber_violence_reports (
    id bigint NOT NULL,
    created_at timestamp with time zone NOT NULL DEFAULT now(),
    violence_type text,
    description text,
    platform text,
    severity text,
    additional_details text,
    status text
);

