    SEQUENCE NAME public.cyber_violence_reports_id_seq
    START WITH 1
    INCREMENT BY 1
    NO MINVALUE
    NO MAXVALUE
    CACHE 1
);

ALTER TABLE ONLY public.cyber_violence_reports
    ADD CONSTRAINT cyber_violence_reports_pkey PRIMARY KEY (id);

-- Enable Row Level Security
ALTER TABLE public.cyber_violence_reports ENABLE ROW LEVEL SECURITY;

